public class Deber1 {
    public static void main(String[] args) {
        byte dato_byte = 100;
        short dato_short = 30000;
        int dato_int = 10000;
        long dato_long = 10000000L;
        float dato_float = 7.877f;
        double dato_double = 17.11;
        char dato_char = 'S';
        boolean dato_boolean = true;
        String dato_string = "hola";
        
        System.out.println("Ejemplo de Byte: " + dato_byte);
        System.out.println("Ejemplo de Short: " + dato_short);
        System.out.println("Ejemplo de Int: " + dato_int);
        System.out.println("Ejemplo de Long: " + dato_long);
        System.out.println("Ejemplo de Float: " + dato_float);
        System.out.println("Ejemplo de Double: " + dato_double);
        System.out.println("Ejemplo de Char: " + dato_char);
        System.out.println("Ejemplo de Boolean: " + dato_boolean);
        System.out.println("Ejemplo de String: " + dato_string);
    }
}
